// Generate varied poetic descriptions
export function generatePoeticNarrative(
  phase: string,
  emotion: string,
  depth: number = 0.5
): string {
  const phaseNarratives = DreamPhaseNarratives[phase as keyof typeof DreamPhaseNarratives] || DreamPhaseNarratives.initiation
  const emotionalLayer = EmotionalLandscapes[emotion as keyof typeof EmotionalLandscapes] || EmotionalLandscapes.mysterious
  
  const randomPhase = phaseNarratives[Math.floor(Math.random() * phaseNarratives.length)]
  const randomEmotion = emotionalLayer[Math.floor(Math.random() * emotionalLayer.length)]
  
  // Add depth-based philosophical layer
  if (depth > 0.7) {
    const philosophies = Object.values(PhilosophicalReflections).flat()
    const philosophy = philosophies[Math.floor(Math.random() * philosophies.length)]
    return `${randomPhase} ${randomEmotion} ${philosophy}`
  }
  
  return `${randomPhase} ${randomEmotion}`
}

// Generate rich sensory descriptions
export function generateSensoryDescription(): string {
  const senses = Object.values(SensoryPalette).flat()
  return senses[Math.floor(Math.random() * senses.length)]
}

// Generate transformation narrative
export function generateTransformationNarrative(type: string = 'emergence'): string {
  const transformations = TransformationImagery[type as keyof typeof TransformationImagery] || TransformationImagery.emergence
  return transformations[Math.floor(Math.random() * transformations.length)]
}